public class Array_sum {
public static void main(String [] args){
    int[] n = {1,2,3,4,5};
int[] m={2,5,6,75,67};
int sum =0;
for(int i :m){
    sum +=i;
}
for(int i :n){
    sum +=i;
}
System.out.print(sum);
}
}
